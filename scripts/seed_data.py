def seed():
    db = SessionLocal()
    try:
        added = 0
        for item in ATTACKS:
            duplicate = (
                db.query(models.Attack)
                .filter(models.Attack.title == item["title"])
                .first()
            )
            if duplicate:
                continue
            attack = models.Attack(**item)
            db.add(attack)
            added += 1
        db.commit()
        print(f"[seed] Successfully seeded {added} attacks.")
    except Exception as e:
        db.rollback()
        print(f"[seed] Error: {e}")
        raise
    finally:
        db.close()


